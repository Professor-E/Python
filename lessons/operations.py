# Arithmetic Operators
# Familiarize yourself with basic arithmetic and mathematical operators

def friends():
    friends = 0
    print(friends)

    # Addition
    friends += 1 # Same as "friends = friends + 1"
    print(friends)

    # Subtraction
    friends -= 2
    print(friends)

    # Addition
    friends += 4
    print(friends)

    # Multiplication
    friends *= 3
    print(friends)

    # Division
    friends /= 2
    print(friends)

    # Remainder
    friends %= 4
    print(friends)

    # Exponentiation
    friends **= 2
    print(friends)


def main():
    # Code
    friends()


if __name__ == "__main__":
    main()