# Variables
# Variables - Store code as placeholders

def main():
    # Strings (Words)
    word = "hello"
    number_string = "4"
    print(f"This is a string: {word}, and a number string: {number_string}")

    # Integers (Whole Numbers)
    a = 4
    b = 5
    print(a, b) # or print(a) \n print(b)

    # Floats (Decimals/Fractions)
    c = 4.28
    d = 6.28
    pi = 3.14159
    print(c, d, pi)

    # Boolean (True/False Values)
    is_bool = True
    is_not_bool = False
    likes_sugar = False
    likes_EECS = True
    print(is_bool, is_not_bool, likes_sugar, likes_EECS)


if __name__ == "__main__":
    main()